// 3. Herança: A tese da herança em O.O, é receber atributos e comportamentos de um objeto pai.
// 4. Polimorfismo: A partir de uma variável de um tipo mais genérico, somos capazes de atribuir vários tipos concretos - ou subtipos - a ela.
